// Get images filtered by gender (includes unisex)
export const getImagesByGender = (gender) => {
  if (!gender) return [];
  return [...designImages[gender], ...designImages.unisex];
};

// Get image by ID
export const getImageById = (id) => {
  const allImages = [...designImages.boy, ...designImages.girl, ...designImages.unisex];
  return allImages.find(img => img.id === id);
};

// Get image names from IDs
export const getImageNames = (ids) => {
  return ids.map(id => {
    const img = getImageById(id);
    return img ? img.name : id;
  });
};
